package Problems;

public class Switch {
    public static void main(String[] args) {
        int a=3;
        switch(a){
            case 1:System.out.println("ok");
            break;
            case 2:System.out.println("ko");
            break;
            case 3:System.out.println("okk");
            break;
            default:System.out.println("not ok");
        }
    }
}
